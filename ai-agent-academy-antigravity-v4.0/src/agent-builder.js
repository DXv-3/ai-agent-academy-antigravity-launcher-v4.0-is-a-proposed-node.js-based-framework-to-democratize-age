// ─── Entry Point ──────────────────────────────────────────────────────────────

if (require.main === module) {
  const prompt = process.argv.slice(2).join(' ');
  buildAgent(prompt).catch((err) => {
    log(`Fatal error: ${err.message}`, 'error');
    process.exit(1);
  });
}

module.exports = { buildAgent, detectModel, loadTemplate, generateAgentCode };
